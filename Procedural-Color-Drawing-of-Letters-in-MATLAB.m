      dot2.slot(1):(dot2.slot(1) + dot2.width - 1), 1) = 255; % Red
image(dot2.slot(2):(dot2.slot(2) + dot2.height - 1), ...
      dot2.slot(1):(dot2.slot(1) + dot2.width - 1), 2) = 255; % Green
