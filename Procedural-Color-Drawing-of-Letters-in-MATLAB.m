% Create a blank color image with dimensions 480x640 pixels and set it to a black background
imageSize = [480, 640, 3]; % Three channels for RGB
background = zeros(imageSize, 'uint8');

% Create the image 
image = background;

% Define the parameters for the letter "S"
letterS = struct('slot', [100, 100], 'gray', 100);

% Draw the top horizontal bar of "S" 
image(letterS.slot(1):letterS.slot(1) + 20, ...
      letterS.slot(2):letterS.slot(2) + 160, 1) = 0; % Red
image(letterS.slot(1):letterS.slot(1) + 20, ...
      letterS.slot(2):letterS.slot(2) + 160, 2) = 255; % Green
image(letterS.slot(1):letterS.slot(1) + 20, ...
      letterS.slot(2):letterS.slot(2) + 160, 3) =255; % Blue

% Draw the left vertical bar of "S" (upper part) 
image(letterS.slot(1):letterS.slot(1) + 100, ...
      letterS.slot(2):letterS.slot(2) + 20, 1) =0; % Red
image(letterS.slot(1):letterS.slot(1) + 100, ...
      letterS.slot(2):letterS.slot(2) + 20, 2) = 255; % Green
image(letterS.slot(1):letterS.slot(1) + 100, ...
      letterS.slot(2):letterS.slot(2) + 20, 3) = 255; % Blue

% Draw the right vertical bar of "S" (lower part)
image(letterS.slot(1) + 80:letterS.slot(1) + 180, ...
      letterS.slot(2) + 140:letterS.slot(2) + 160, 1) = 0; % Red
image(letterS.slot(1) + 80:letterS.slot(1) + 180, ...
      letterS.slot(2) + 140:letterS.slot(2) + 160, 2) = 255; % Green
image(letterS.slot(1) + 80:letterS.slot(1) + 180, ...
      letterS.slot(2) + 140:letterS.slot(2) + 160, 3) = 255; % Blue

% Draw the bottom horizontal bar of "S" 
image(letterS.slot(1) + 160:letterS.slot(1) + 180, ...
      letterS.slot(2):letterS.slot(2) + 160, 1) = 0; % Red
image(letterS.slot(1) + 160:letterS.slot(1) + 180, ...
      letterS.slot(2):letterS.slot(2) + 160, 2) = 255; % Green
image(letterS.slot(1) + 160:letterS.slot(1) + 180, ...
      letterS.slot(2):letterS.slot(2) + 160, 3) = 255; % Blue

% Connect the dots in the middle part of "S" with a vertical line 
image(letterS.slot(1) + 80:letterS.slot(1) + 100, ...
      letterS.slot(2) + 20:letterS.slot(2) + 140, 1) = 0; % Red
image(letterS.slot(1) + 80:letterS.slot(1) + 100, ...
      letterS.slot(2) + 20:letterS.slot(2) + 140, 2) = 255; % Green
image(letterS.slot(1) + 80:letterS.slot(1) + 100, ...
      letterS.slot(2) + 20:letterS.slot(2) + 140, 3) = 255; % Blue

% Define the parameters for the letter "Y"
letterY = struct('slot', [100, 320], 'gray', 50);

% Add the upper left vertical bar of "Y"
image(letterY.slot(1):(letterY.slot(1) + 100), ...
      letterY.slot(2):(letterY.slot(2) + 20), 1) = 0; % Red
image(letterY.slot(1):(letterY.slot(1) + 100), ...
      letterY.slot(2):(letterY.slot(2) + 20), 2) = 255; % Green
image(letterY.slot(1):(letterY.slot(1) + 100), ...
      letterY.slot(2):(letterY.slot(2) + 20), 3) = 0; % Blue

% Add the bottom bar of "Y"
image(letterY.slot(1) + 160:(letterY.slot(1) + 180), ...
      letterY.slot(2):(letterY.slot(2) + 160), 1) = 0; % Red
image(letterY.slot(1) + 160:(letterY.slot(1) + 180), ...
      letterY.slot(2):(letterY.slot(2) + 160), 2) = 255; % Green
image(letterY.slot(1) + 160:(letterY.slot(1) + 180), ...
      letterY.slot(2):(letterY.slot(2) + 160), 3) = 0; % Blue

% Add the right vertical bar of "Y"
image(letterY.slot(1):(letterY.slot(1) + 180), ...
      letterY.slot(2) + 140:(letterY.slot(2) + 160), 1) = 0; % Red
image(letterY.slot(1):(letterY.slot(1) + 180), ...
      letterY.slot(2) + 140:(letterY.slot(2) + 160), 2) = 255; % Green
image(letterY.slot(1):(letterY.slot(1) + 180), ...
      letterY.slot(2) + 140:(letterY.slot(2) + 160), 3) = 0; % Blue

% Connect the dots in the middle part of "S" with a vertical line 
