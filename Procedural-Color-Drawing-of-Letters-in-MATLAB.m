      dot1.slot(1):(dot1.slot(1) + dot1.width - 1), 1) = 255; % Red
image(dot1.slot(2):(dot1.slot(2) + dot1.height - 1), ...
      dot1.slot(1):(dot1.slot(1) + dot1.width - 1), 2) = 0; % Green
